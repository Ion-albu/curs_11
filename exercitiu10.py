# Exercițiul 10: Statistici de apariție
# Condiția: Într-un tuplu cu rezultatele unor aruncări de zar,
# numără câte ori a ieșit numărul 6.

aruncari_zar = (1, 6, 3, 6, 2, 4, 6, 1, 5, 6, 3, 6, 2)
Statistica_de_aruncare = aruncari_zar.count(6)
print(f"la aruncarea unui zar numarul 6 a aparut de: {Statistica_de_aruncare} ori")